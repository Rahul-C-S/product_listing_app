class Product {
  final int id;
  final String name;
  final double mrp;
  final double price;
  final bool isActive;
  final bool inWishlist;
  final String image;
  final double rating;

  Product({
    required this.id,
    required this.name,
    required this.mrp,
    required this.price,
    required this.isActive,
    required this.inWishlist,
    required this.image,
    required this.rating,
  });

}
